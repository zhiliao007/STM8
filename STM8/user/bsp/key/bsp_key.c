/**
  * 函数功能: 初始化外部中断函数
  * 输入参数: 无
  * 返 回 值: 无
  * 说    明：PC5配置为上拉输入有中断，中断触发方式为下降沿触发
  */
void EXIT_GPIO_Init(void)  
{   
    GPIO_Init(GPIOC, GPIO_PIN_5, GPIO_MODE_IN_PU_IT);
    EXTI_SetExtIntSensitivity(EXTI_PORT_GPIOC, EXTI_SENSITIVITY_FALL_ONLY);
    enableInterrupts();
}  


